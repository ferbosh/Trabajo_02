##INGRESE EL DIA DE LA SEMANA

DIA = int(input("INGRESE EL DIA DE LA SEMANA:"))
if DIA == 1:
    print("HOY ES LUNES")
elif DIA == 2 :
    print("HOY ES MARTES")
elif DIA == 3 :
    print("HOY ES MIERCOLES")
elif DIA == 4:
    print("HOY ES JUEVES")
elif DIA == 5:
    print("HOY ES VIERNES")
elif DIA == 6 :
    print("HOY ES SABADO")
elif DIA == 7 :
    print("HOY ES DOMINGO")
else :
    print("EL DIA NO INGRESADO NO EXISTE")
